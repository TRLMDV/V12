export type TranslationKeys = keyof typeof i18n.en;

export function useTranslation() {
  const { settings } = useData();
  const currentLang = settings.language || 'en'; // Fallback to English

  const t = useMemo(() => {
    const translations = i18n[currentLang] || i18n.en; // Fallback to English if currentLang is missing

    return (key: TranslationKeys, replacements?: { [key: string]: string | number }): string => {
      let text = translations[key] || i18n.en[key] || key; // Fallback to English key, then key itself
      if (replacements) {
        for (const placeholder in replacements) {
          text = text.replace(`{${placeholder}}`, String(replacements[placeholder]));
        }
      }
      return text;
    };
  }, [currentLang]);

  return { t, currentLang };
}

export function getKeyAsPageId(key: string): string {
  // from camelCase to kebab-case, e.g., purchaseOrders -> purchase-orders
  return key.replace(/([a-z0-9]|(?=[A-Z]))([A-Z])/g, '$1-$2').toLowerCase();
}